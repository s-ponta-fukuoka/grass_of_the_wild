#include "canvas.h"

//*****************************************************************************
// マクロ定義
//*****************************************************************************

///////////////////////////////////////////////////////////////////////////////
//コンストラクタ
///////////////////////////////////////////////////////////////////////////////
Canvas::Canvas()
{
	m_pVertexBuffer = NULL;
	m_pIndexBuffer = NULL;
}

///////////////////////////////////////////////////////////////////////////////
//デストラクタ
///////////////////////////////////////////////////////////////////////////////
Canvas::~Canvas()
{
	Release();
}

///////////////////////////////////////////////////////////////////////////////
//初期化
///////////////////////////////////////////////////////////////////////////////
HRESULT Canvas::Init(void)
{
	Object::Init();
	return S_OK;
}

///////////////////////////////////////////////////////////////////////////////
//終了
///////////////////////////////////////////////////////////////////////////////
void Canvas::Release(void)
{
	ES_SAFE_RELEASE(m_pVertexBuffer);
	ES_SAFE_RELEASE(m_pIndexBuffer);

	Object::Release();
}

///////////////////////////////////////////////////////////////////////////////
//更新
///////////////////////////////////////////////////////////////////////////////
void Canvas::Update(void)
{
	Object::Update();
}
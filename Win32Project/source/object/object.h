class Object
{
public:
	//transform
	struct Transform
	{
		VECTOR3	position;
		VECTOR3 rot;
		VECTOR3 scale;
	};

	//コンストラクタ
	Object();

	//デストラクタ
	virtual ~Object();

	//初期化
	virtual HRESULT Init(void);

	//終了
	virtual void Release(void);

	//更新
	virtual void Update(void);

	//トランスフォーム取得
	Transform* GetTransform(void);

protected:
	Transform*				m_pTransform;
};


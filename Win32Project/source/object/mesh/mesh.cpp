#include "mesh.h"

//*****************************************************************************
// マクロ定義
//*****************************************************************************

///////////////////////////////////////////////////////////////////////////////
//コンストラクタ
///////////////////////////////////////////////////////////////////////////////
Mesh::Mesh()
{ 
	m_pVertexBuffer = NULL;
	m_pIndexBuffer = NULL;
}

///////////////////////////////////////////////////////////////////////////////
//デストラクタ
///////////////////////////////////////////////////////////////////////////////
Mesh::~Mesh()
{
	Release();
}

///////////////////////////////////////////////////////////////////////////////
//初期化
///////////////////////////////////////////////////////////////////////////////
HRESULT Mesh::Init(void)
{
	Object::Init();
	return S_OK;
}

///////////////////////////////////////////////////////////////////////////////
//終了
///////////////////////////////////////////////////////////////////////////////
void Mesh::Release(void)
{
	ES_SAFE_RELEASE(m_pVertexBuffer);
	ES_SAFE_RELEASE(m_pIndexBuffer);

	Object::Release();
}

///////////////////////////////////////////////////////////////////////////////
//更新
///////////////////////////////////////////////////////////////////////////////
void Mesh::Update(void)
{
	Object::Update();
}